/*Interpolation is a way to dynamically insert variables or expressions into a string. it allows you to create a string
 literal with placeholders,  denoted by `${}`The placeholders are then replaced with the evaluated values of the variables
 or expressions. This provides a concise and readable way to build strings. */

const name = "Akshata";
const age = 22;


const message = `My name is ${name} and I am ${age} years old.`;

console.log(message); 
